"""
Tipo None

O tipo de dado None em Python representa o tipo "sem tipo" ou poderia ser conhecido tb como tipo vazio
mas falar que é um tipo "sem tipo" é mais apropriado.

OBS:
- O tipo None é sempre especificado com a primeira letra maiúscula como os tipos booleans
- O tipo None em Python é sempre considerado como False

Utilizamos quando:
- Queremos criar uma variável e inicializá-la com um tipo "sem tipo", antes de receber um valor final
"""
numeros = None

print(numeros)
print(type(numeros))

